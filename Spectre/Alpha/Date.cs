        private int GetDatePart(DatePart Part)
        {
            return this.GetDateParts()[(int)Part];
        }

        private int[] GetDateParts()
        {

            int N400 = this._ticks / DAYS_PER_400YEARS;
            int REM400 = this._ticks % DAYS_PER_400YEARS;
            int N100 = REM400 / DAYS_PER_100YEARS;
            int REM100 = REM400 % DAYS_PER_100YEARS;
            int N4 = REM100 / DAYS_PER_4YEARS;
            int REM4 = REM100 % DAYS_PER_4YEARS;
            int N1 = REM4 / DAYS_PER_YEAR;
            int REM1 = REM4 % DAYS_PER_YEAR;

            int Year = N400 * 400 + N100 * 100 + N4 * 4 + N1 * 1 + 1;
            int DayOfYear = REM1;
            bool il = IsLeap(Year);
            int Month = (il ? MonthLeapHashTable[DayOfYear] : MonthHashTable[DayOfYear]);
            int Day = (il ? DayLeapHashTable[DayOfYear] : DayHashTable[DayOfYear]); ;
            
            return new int[] { Year, Month, Day, DayOfYear };

        }

        private static int ToBinary(int Year, int Month, int Day)
        {
            int N400 = Year / 400;
            int R400 = Year % 400;
            int N100 = R400 / 100;
            int R100 = R400 % 100;
            int N4 = R100 / 4;
            int R4 = R100 % 4;
            int y = N400 * DAYS_PER_400YEARS + N100 * DAYS_PER_100YEARS + N4 * DAYS_PER_4YEARS + Days[R4];
            bool il = IsLeap(Year);
            int d = (il ? LeapSerialHashTable[Month - 1][Day - 1] : SerialHashTable[Month - 1][Day - 1]);
            return y + d;
        }

        private static bool IsLeap(int Year)
        {
            return (Year % 4 == 0) && (Year % 400 == 0 || Year % 100 != 0);

        }

        









