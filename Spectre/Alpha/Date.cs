        public override string ToString()
        {
            int[] x = this.GetDateParts();
            return string.Format("{0}-{1}-{2}", x[0], x[1] < 10 ? "0" + x[1].ToString() : x[1].ToString(), x[2] < 10 ? "0" + x[2].ToString() : x[2].ToString());
        }

